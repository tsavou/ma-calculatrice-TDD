export class Calculatrice {


    add(a: number, b: number): number {
        const result = a + b;
        return result;
    }


    subtract(a: number, b: number) {
        const result = a - b;
        return result;
    }

    multiply(a: number, b: number) {
        const result = a * b;
        return result;
    }
}
